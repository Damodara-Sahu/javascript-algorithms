// stack
let stack = [];
// put value on top of stack
stack.push(2);
stack.push(5);
// remove value from top of stack
const i = stack.pop();
console.log(i)

// queue
let queue = [];
// put value on end of queue
queue.push(2);
queue.push(5);
// take first value from queue
const j= queue.unshift();
console.log(j)
